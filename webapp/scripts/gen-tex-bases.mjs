/**
 * Generate static .tex base files for all 4 resume types.
 * Run from webapp/ with:  node scripts/gen-tex-bases.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { parse } from 'node-html-parser';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DATA_DIR   = path.join(__dirname, '..', 'src', 'data');

// ── LaTeX helpers (duplicated from lib/latex.ts to keep this script self-contained) ──

function esc(s) {
  return s
    .replace(/\\/g, '\\textbackslash{}')
    .replace(/&/g,  '\\&')
    .replace(/%/g,  '\\%')
    .replace(/\$/g, '\\$')
    .replace(/#/g,  '\\#')
    .replace(/_/g,  '\\_')
    .replace(/\{/g, '\\{')
    .replace(/\}/g, '\\}')
    .replace(/~/g,  '\\textasciitilde{}')
    .replace(/\^/g, '\\textasciicircum{}')
    .replace(/</g,  '\\textless{}')
    .replace(/>/g,  '\\textgreater{}')
    .replace(/–/g,  '--')
    .replace(/—/g,  '---')
    .replace(/'/g,  "'")
    .replace(/'/g,  "'")
    .replace(/"/g,  "''")
    .replace(/"/g,  "``")
    .replace(/…/g,  '\\ldots{}')
    .replace(/₹/g,  '\\rupee{}')
    .replace(/•/g,  '\\textbullet{}');
}

function txt(el) {
  return el?.text?.trim() ?? '';
}

function buildLatex(html) {
  const root = parse(html);
  const lines = [];

  lines.push(`% ── Generated by Job Tailor · jobtailor.in ────────────────────────────────
% Upload this file to https://overleaf.com → New Project → Upload → Recompile
% ─────────────────────────────────────────────────────────────────────────────
\\documentclass[10pt,letterpaper]{article}

\\usepackage[top=0.5in,bottom=0.5in,left=0.65in,right=0.65in]{geometry}
\\usepackage[hidelinks]{hyperref}
\\usepackage{enumitem}
\\usepackage{titlesec}
\\usepackage[T1]{fontenc}
\\usepackage[utf8]{inputenc}
\\usepackage{microtype}
\\usepackage{xcolor}

% ── Typography ────────────────────────────────────────────────────────────────
\\pagestyle{empty}
\\setlength{\\parindent}{0pt}
\\setlength{\\parskip}{0pt}
\\hypersetup{colorlinks=true,urlcolor=black,linkcolor=black}

% ── Section title ─────────────────────────────────────────────────────────────
\\titleformat{\\section}
  {\\vspace{2pt}\\scshape\\raggedright\\large\\bfseries}
  {}{0em}{}[\\color{black}\\titlerule\\vspace{2pt}]
\\titlespacing*{\\section}{0pt}{5pt}{3pt}

% ── Bullet list ───────────────────────────────────────────────────────────────
\\setlist[itemize]{leftmargin=0.18in,label=\\textbullet,itemsep=1pt,parsep=0pt,topsep=2pt}

% ── Custom commands ───────────────────────────────────────────────────────────
\\newcommand{\\entry}[3]{%
  \\vspace{3pt}\\textbf{#1} \\hfill #3 \\\\
  \\textit{\\small #2}\\vspace{1pt}}
\\newcommand{\\skillrow}[2]{\\textbf{#1:} #2 \\\\[1pt]}

\\begin{document}
`);

  const name = txt(root.querySelector('h1'));
  const targetRole = txt(root.querySelector('.target-role'));
  lines.push(`% ── HEADER ──────────────────────────────────────────────────────────────────`);
  lines.push(`\\begin{center}`);
  lines.push(`  {\\Huge\\bfseries ${esc(name)}}\\\\[3pt]`);
  if (targetRole) {
    lines.push(`  {\\large\\textit{${esc(targetRole)}}}\\\\[3pt]`);
  }

  const contactParts = [];
  root.querySelectorAll('.contact-item').forEach((item) => {
    const link = item.querySelector('a');
    const span = item.querySelector('span');
    if (link) {
      const href = link.getAttribute('href') ?? '';
      const label = link.text?.trim() ?? '';
      if (href.startsWith('mailto:')) {
        contactParts.push(`\\href{${href}}{${esc(label)}}`);
      } else if (href.startsWith('http')) {
        contactParts.push(`\\href{${href}}{${esc(label)}}`);
      } else {
        contactParts.push(esc(label));
      }
    } else if (span) {
      contactParts.push(esc(span.text?.trim() ?? ''));
    }
  });

  if (contactParts.length) {
    lines.push(`  \\small ${contactParts.join(' $\\vert$\\; ')}`);
  }
  lines.push(`\\end{center}\n`);

  const sections = root.querySelectorAll('.section');

  for (const sec of sections) {
    const secTitle = sec.querySelector('.section-title')?.text?.trim() ?? '';
    if (!secTitle) continue;
    const lower = secTitle.toLowerCase();

    lines.push(`% ── ${secTitle.toUpperCase()} ${'─'.repeat(Math.max(0, 72 - secTitle.length))}`);
    lines.push(`\\section{${esc(secTitle)}}\n`);

    if (lower === 'summary') {
      const p = sec.querySelector('p');
      const summaryText = txt(p);
      if (summaryText) lines.push(esc(summaryText) + '\n');

    } else if (lower === 'skills') {
      sec.querySelectorAll('.skill-row').forEach((row) => {
        const cat = row.querySelector('.skill-cat')?.text?.trim() ?? '';
        const val = row.querySelector('.skill-val')?.text?.trim() ?? '';
        lines.push(`\\skillrow{${esc(cat)}}{${esc(val)}}`);
      });
      lines.push('');

    } else if (lower === 'experience') {
      sec.querySelectorAll('.job').forEach((job) => {
        const title   = job.querySelector('.job-title')?.text?.trim()   ?? '';
        const date    = job.querySelector('.job-date')?.text?.trim()    ?? '';
        const company = job.querySelector('.job-company')?.text?.trim() ?? '';
        lines.push(`\\entry{${esc(title)}}{${esc(company)}}{${esc(date)}}`);
        lines.push(`\\begin{itemize}`);
        job.querySelectorAll('li').forEach((li) => {
          lines.push(`  \\item ${esc(li.text.trim())}`);
        });
        lines.push(`\\end{itemize}\n`);
      });

    } else if (lower === 'projects') {
      sec.querySelectorAll('.project-block').forEach((proj) => {
        const projName = proj.querySelector('.project-name')?.text?.trim() ?? '';
        const projDate = proj.querySelector('.job-date')?.text?.trim()    ?? '';
        const linkEls  = proj.querySelectorAll('.project-links a');
        const linkParts = linkEls.map((a) => {
          const href  = a.getAttribute('href') ?? '';
          const label = a.text.trim();
          return href ? `\\href{${esc(href)}}{${esc(label)}}` : esc(label);
        });
        const linksStr = linkParts.join(' \\quad\\textbullet\\quad ');
        lines.push(`\\entry{${esc(projName)}}{${linksStr || ''}}{${esc(projDate)}}`);
        lines.push(`\\begin{itemize}`);
        proj.querySelectorAll('li').forEach((li) => {
          lines.push(`  \\item ${esc(li.text.trim())}`);
        });
        lines.push(`\\end{itemize}\n`);
      });

    } else if (lower === 'education') {
      const degree = sec.querySelector('.edu-degree')?.text?.trim() ?? '';
      const year   = sec.querySelector('.edu-year')?.text?.trim()   ?? '';
      const school = sec.querySelector('.edu-school')?.text?.trim() ?? '';
      lines.push(`\\entry{${esc(degree)}}{${esc(school)}}{${esc(year)}}`);
      const lis = sec.querySelectorAll('li');
      if (lis.length) {
        lines.push(`\\begin{itemize}`);
        lis.forEach((li) => lines.push(`  \\item ${esc(li.text.trim())}`));
        lines.push(`\\end{itemize}`);
      }
      lines.push('');

    } else {
      sec.querySelectorAll('.award-header').forEach((hdr, i) => {
        const aTitle = hdr.querySelector('.award-title')?.text?.trim() ?? '';
        const aYear  = hdr.querySelector('.award-year')?.text?.trim()  ?? '';
        const nextEl = hdr.nextElementSibling;
        const org    = nextEl?.classList?.contains('award-org') ? nextEl.text.trim() : '';

        if (i > 0) lines.push('');
        lines.push(`\\textbf{${esc(aTitle)}} \\hfill ${esc(aYear)} \\\\`);
        if (org) lines.push(`\\textit{${esc(org)}}`);

        const afterOrg = org ? nextEl?.nextElementSibling : nextEl;
        if (afterOrg?.tagName === 'UL') {
          lines.push(`\\begin{itemize}`);
          afterOrg.querySelectorAll('li').forEach((li) => {
            lines.push(`  \\item ${esc(li.text.trim())}`);
          });
          lines.push(`\\end{itemize}`);
        }
      });
      lines.push('');
    }
  }

  lines.push(`\\end{document}`);
  return lines.join('\n');
}

